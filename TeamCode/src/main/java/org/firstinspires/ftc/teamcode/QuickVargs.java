package org.firstinspires.ftc.teamcode;

public class QuickVargs {
    public int[] arg(int...ints){
        return ints;
    }
    public double[] arg(double ... doubles){
        return doubles;
    }
    public long[] arg(long ... longs) {
        return longs;
    }
    public byte[] arg(byte...bytes   ){
        return bytes;
    }
    public short[] arg(short...shorts){
        return shorts;
    }
    public float[] arg(float...floats){
        return floats;
    }
    public boolean[] arg(boolean...booleans){
        return booleans;
    }
    public char[] arg(char...chars){
        return chars;
    }
    public String[] arg(String ... strings){
        return strings;
    }
    public Object[] arg(Object...objects)  {
        return objects;
    }
    public Void[] arg(){
        return null;
    }
}
